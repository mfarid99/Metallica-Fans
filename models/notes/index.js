///////////////////////////////////
// IMPORT SCHEMA AND MODEL
///////////////////////////////////
const { Schema, model } = require("mongoose");

////////////////////////////////////
// CREATE SCHEMA
////////////////////////////////////

const noteSchema = new Schema({
  username: { type: String, required: true},
  note: String,
}, {timestamps: true});

////////////////////////////////////
// CREATE MODEL
////////////////////////////////////
const Note = model("note", noteSchema);

////////////////////////////////////
// EXPORT MODEL
////////////////////////////////////
module.exports = Note;
